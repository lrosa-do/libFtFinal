#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (lst == NULL || del == NULL)
		return ;
	del(lst->content);
	lst->content = NULL;
	ft_free(lst);
	lst = NULL;
}
